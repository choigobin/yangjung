package p0702;

public class hello {

	public static void main(String[] args) {
		int num1;
		int num2;
		int result;
		num1=10;
		num2=20;
		result=num1/num2;
		System.out.print("HELLOW WORLD\n");
		System.out.println("HELLOW WORLD");
		System.out.println("num1:"+num1);
		System.out.println("num2:"+num2);
		System.out.println("result:"+result);
		System.out.println("result:"+10/20);
		System.out.println("result:"+10.0/20);
		System.out.println("result:"+10/20.0);
		System.out.println("result:"+1.0/3);
		System.out.println("result:"+(float)10/20);
		System.out.println("result:"+(int)10.0/20);
		System.out.printf("%d+%d=%d",num1,num2,result);
	}

}
